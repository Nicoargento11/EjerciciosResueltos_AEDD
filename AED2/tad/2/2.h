
float sumarDosNumeros(float, float);
float restarDosNumeros(float, float);
float multiplicarDosNumeros(float, float);
float dividirDosNumeros(float, float);

float sumarDosNumeros(float pNum1, float pNum2)
{
    return pNum1 + pNum2;
}

float restarDosNumeros(float pNum1, float pNum2)
{
    return pNum1 - pNum2;
}
float multiplicarDosNumeros(float pNum1, float pNum2)
{
    return pNum1 * pNum2;
}
float dividirDosNumeros(float pNum1, float pNum2)
{
    return (float)pNum1 / pNum2;
}